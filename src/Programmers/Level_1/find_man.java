import java.util.*;
class Solution {
    public String solution(String[] seoul) {
        StringBuffer answer =new StringBuffer();
        ArrayList<String>  s = new ArrayList<>(Arrays.asList(seoul));
        int i = s.indexOf("Kim");
        answer.append("김서방은 ");
        answer.append(Integer.toString(i));
        answer.append("에 있다");
                
        return answer.toString();
    }
}
